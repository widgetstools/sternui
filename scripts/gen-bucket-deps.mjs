#!/usr/bin/env node
/**
 * gen-bucket-deps.mjs — produce ng-package.json + react-package.json at
 * the repo root. Each is the union of every dependency / devDependency
 * / peerDependency declared by the relevant `packages/*` subtree
 * (excluding `@starui/*` internal refs, which resolve via the
 * workspace).
 *
 * Buckets:
 *   ng-package.json    — packages/shared/** + packages/angular/**
 *   react-package.json — packages/shared/** + packages/react/**
 *
 * The output files mimic a regular package.json shape so an operator
 * can run `npm install` against either one in an isolated directory
 * to confirm every external dep is reachable from the corporate
 * artifactory.
 *
 * Conflict policy: when two source package.json files declare the
 * same external dep at different versions, the LAST-encountered spec
 * wins (paths are walked in sorted order, so the result is
 * deterministic). All conflicts are reported on stderr.
 *
 * Re-runnable: usage `node scripts/gen-bucket-deps.mjs`.
 */

import {
  readFileSync,
  readdirSync,
  writeFileSync,
  statSync,
} from 'node:fs';
import { join, resolve } from 'node:path';

const REPO_ROOT = resolve(import.meta.dirname, '..');

const BUCKETS = {
  'ng-package': ['packages/shared', 'packages/angular'],
  'react-package': ['packages/shared', 'packages/react'],
};

function isDir(p) {
  try { return statSync(p).isDirectory(); } catch { return false; }
}

// Recursively find every package.json under `root`, skipping
// node_modules, dist (build output that copies its own package.json),
// and any path segment named `.turbo`.
function findPackageJsons(root) {
  const out = [];
  function walk(dir) {
    let entries;
    try { entries = readdirSync(dir, { withFileTypes: true }); }
    catch { return; }
    for (const e of entries) {
      if (e.name === 'node_modules' || e.name === 'dist' || e.name === '.turbo') continue;
      const p = join(dir, e.name);
      if (e.isDirectory()) walk(p);
      else if (e.name === 'package.json') out.push(p);
    }
  }
  walk(root);
  return out.sort();
}

function mergeSection(into, sectionObj, conflicts, sourcePath, sectionName) {
  if (!sectionObj || typeof sectionObj !== 'object') return;
  for (const [name, version] of Object.entries(sectionObj)) {
    if (name.startsWith('@starui/')) continue; // workspace-internal, skip
    if (into[name] && into[name] !== version) {
      conflicts.push({
        name,
        existing: into[name],
        incoming: version,
        section: sectionName,
        source: sourcePath,
      });
    }
    into[name] = version;
  }
}

function collect(rootDirs) {
  const deps = {};
  const devDeps = {};
  const conflicts = [];
  for (const rel of rootDirs) {
    const root = join(REPO_ROOT, rel);
    if (!isDir(root)) continue;
    for (const f of findPackageJsons(root)) {
      const rawText = readFileSync(f, 'utf8');
      let pkg;
      try { pkg = JSON.parse(rawText); }
      catch { continue; }
      const relPath = f.slice(REPO_ROOT.length + 1);
      // Promote peerDependencies into the regular deps bucket — to
      // install + build, peers must be present in the dep tree.
      mergeSection(deps, pkg.dependencies, conflicts, relPath, 'dependencies');
      mergeSection(deps, pkg.peerDependencies, conflicts, relPath, 'peerDependencies');
      mergeSection(devDeps, pkg.devDependencies, conflicts, relPath, 'devDependencies');
    }
  }
  return { deps, devDeps, conflicts };
}

function readRootDevDeps() {
  const root = JSON.parse(readFileSync(join(REPO_ROOT, 'package.json'), 'utf8'));
  return root.devDependencies ?? {};
}

function sortObj(o) {
  return Object.fromEntries(Object.entries(o).sort(([a], [b]) => a.localeCompare(b)));
}

function buildManifest(name, deps, devDeps) {
  return {
    name,
    version: '0.0.0',
    private: true,
    description: `Generated by scripts/gen-bucket-deps.mjs — union of external (non-@starui/*) deps + devDeps + peerDeps from the corresponding packages/* subtree. Install against the corporate artifactory to verify availability. Do not edit by hand.`,
    dependencies: sortObj(deps),
    devDependencies: sortObj(devDeps),
  };
}

function main() {
  const rootDevDeps = readRootDevDeps();
  for (const [bucketName, rootDirs] of Object.entries(BUCKETS)) {
    const { deps, devDeps, conflicts } = collect(rootDirs);
    // Merge root devDeps into each bucket — turbo + shared build tooling
    // is required to build every workspace.
    for (const [k, v] of Object.entries(rootDevDeps)) {
      if (!devDeps[k]) devDeps[k] = v;
    }
    const manifest = buildManifest(bucketName, deps, devDeps);
    const outPath = join(REPO_ROOT, `${bucketName}.json`);
    writeFileSync(outPath, JSON.stringify(manifest, null, 2) + '\n');
    process.stdout.write(
      `[gen-bucket-deps] wrote ${bucketName}.json — ` +
      `${Object.keys(manifest.dependencies).length} deps, ` +
      `${Object.keys(manifest.devDependencies).length} devDeps\n`,
    );
    if (conflicts.length) {
      process.stderr.write(`\n[gen-bucket-deps] ${conflicts.length} version conflicts in ${bucketName} (last spec wins):\n`);
      for (const c of conflicts) {
        process.stderr.write(`  ${c.name} (${c.section}): ${c.existing} → ${c.incoming} [from ${c.source}]\n`);
      }
    }
  }
}

main();
